def organizadora(lista):
    SORT_ORDER = {"M":0,"T":1,"N":2}
    turnos = []
    organizada = []
    for elem in lista:
        index = lista.index(elem)
        horario = elem.split()
        turno = horario[0][1]
        tupla = (turno,index)
        turnos.append(tupla)
    turnos = sorted(turnos, key=lambda val: SORT_ORDER[val[0]])
    for tupla in turnos:
        index = tupla[1]
        organizada.append(lista[index])
    return organizada

